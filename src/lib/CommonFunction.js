export const getServerName = (key) => {
  switch (key) {
    case 'cain':
      return '카인';
    case 'diregie':
      return '디레지에';
    case 'siroco':
      return '시로코';
    case 'prey':
      return '프레이';
    case 'casillas':
      return '카시야스';
    case 'hilder':
      return '힐더';
    case 'anton':
      return '안톤';
    case 'bakal':
      return 'bakal';
    default:
      break;
  }
};